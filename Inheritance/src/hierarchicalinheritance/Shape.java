package hierarchicalinheritance;

/**
 * Represents a shape with a color.
 * Provides a method to display the color.
 * 
 * @author C Sandeep Aithal
 */
public class Shape {
    String color;

    /**
     * Displays the color of the shape.
     */
    public void display() {
        System.out.println("Color: " + color);
    }
}


